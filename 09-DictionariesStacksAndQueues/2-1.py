#Operacje na zbiorach:

#& (Iloczyn): Znajduje wspólne elementy w zbiorach.
#| (Suma): Łączy wszystkie elementy z obu zbiorów (usuwa duplikaty).
#- (Różnica): Znajduje elementy obecne w jednym zbiorze, ale nie w drugim.
#^ (Różnica symetryczna): Znajduje elementy obecne w dowolnym zbiorze, ale nie w obu.
#.issubset(): Sprawdza, czy jeden zbiór jest podzbiorem drugiego.
